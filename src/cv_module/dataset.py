"""
Dataset utilities for BraTS2020.

Provides two dataset backends:

1. PreprocessedDataset (default)
   Reads serialized .pt files from datasets/processed/cv/
   Generated by Notebook 06.5 — fast loading, no on-the-fly preprocessing.
   Random patch sampling and augmentation are applied by get_patch_transforms.

2. RawDataset (fallback)
   Reads raw NIfTI files and applies the full MONAI transform pipeline.
   Used only when .pt files are not available.

Design:
    CacheDataset applies the full transform pipeline including random patch
    sampling and augmentation for training. Only deterministic stages benefit
    from caching — MONAI's CacheDataset caches up to the first random transform
    in a Compose automatically.

    Validation and test datasets use deterministic transforms only.
    Full cropped volumes are passed to SlidingWindowInferer during validation.

Author:
Mariam Mohamed
"""

