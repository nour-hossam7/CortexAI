"""
Dataset utilities for TextBraTS report embeddings.

Provides:

- Loading precomputed train/validation/test embeddings (.npy) generated
  by Notebooks 06a/06b, indexed by patient_id
- Loading the cleaned report CSVs generated by Notebooks 03/04, for
  building embeddings on demand (e.g. with a different MODEL_NAME)

Design mirrors cv_module.dataset: a "PreprocessedDataset"-style reader
for the fast path (precomputed .npy), with patient_id used as the join
key throughout — embeddings and metadata files are written in matching
row order by Notebooks 06a/06b, but row order is NOT guaranteed to
match the CV module's .pt file order, so the fusion module (Ammar)
should always join on patient_id rather than position.

Author:
Nour Hossam
"""

from pathlib import Path
from typing import Dict

import numpy as np
import pandas as pd

from .config import Config

__all__ = [
    "load_clean_reports",
    "load_text_embeddings",
    "get_patient_embedding",
    "build_embedding_lookup",
]


def load_clean_reports(subset: str) -> pd.DataFrame:
    """
    Load the cleaned reports CSV for one subset.

    Generated by Notebook 04 (medical-text-preprocessing).

    Parameters
    ----------
    subset : str
        One of "train", "validation", "test".

    Returns
    -------
    pd.DataFrame
        Columns include "patient_id", "report", "clean_report".
    """

    if subset not in ("train", "validation", "test"):
        raise ValueError(f"Unknown subset '{subset}'.")

    path = Config.PROCESSED_REPORTS_DIR / f"{subset}_reports_clean.csv"

    return pd.read_csv(path)


def load_text_embeddings(
    subset: str,
    model_name: str | None = None,
) -> Dict[str, np.ndarray]:
    """
    Load precomputed text embeddings for one subset, indexed by
    patient_id.

    Generated by Notebook 06a (BioBERT) / 06b (ClinicalBERT). Reads
    the embeddings .npy file and its matching metadata .csv together
    and zips them into a dict, so callers never have to reason about
    row order themselves.

    Parameters
    ----------
    subset : str
        One of "train", "validation", "test".
    model_name : str | None
        HuggingFace model identifier whose embedding folder to read.
        Defaults to Config.MODEL_NAME. Must be one of
        Config.AVAILABLE_MODELS.

    Returns
    -------
    dict
        {patient_id: embedding} where embedding is a 1-D np.ndarray of
        shape (Config.EMBEDDING_DIM,).

    Raises
    ------
    AssertionError
        If the number of embedding rows does not match the number of
        metadata rows (would indicate a corrupted or mismatched pair
        of files).
    """

    if subset not in ("train", "validation", "test"):
        raise ValueError(f"Unknown subset '{subset}'.")

    if model_name is None:
        model_name = Config.MODEL_NAME

    if model_name not in Config.AVAILABLE_MODELS:
        raise ValueError(
            f"'{model_name}' is not one of Config.AVAILABLE_MODELS: "
            f"{Config.AVAILABLE_MODELS}."
        )

    model_folder = model_name.split("/")[-1]
    base_dir = Config.EMBEDDINGS_DIR / model_folder

    embeddings = np.load(base_dir / f"{subset}_embeddings.npy")
    metadata = pd.read_csv(base_dir / f"{subset}_metadata.csv")

    assert embeddings.ndim == 2, (
        f"Expected a 2-D embedding matrix, got "
        f"shape {embeddings.shape}."
    )

    assert embeddings.shape[1] == Config.EMBEDDING_DIM, (
        f"Embedding dimension mismatch. "
        f"Expected {Config.EMBEDDING_DIM}, "
        f"got {embeddings.shape[1]}."
    )

    assert len(embeddings) == len(metadata), (
        f"Mismatch in '{subset}' subset for model '{model_name}': "
        f"{len(embeddings)} embedding rows vs {len(metadata)} metadata "
        f"rows. These files must be regenerated together (Notebook "
        f"06a/06b) — never edit one without the other."
    )

    patient_ids = metadata["patient_id"].tolist()

    return dict(zip(patient_ids, embeddings))


def build_embedding_lookup(
    model_name: str | None = None,
) -> Dict[str, np.ndarray]:
    """
    Load and merge embeddings for ALL THREE subsets into a single
    patient_id -> embedding dict.

    Convenience for the fusion module: most fusion code just needs
    "give me this patient's text feature vector" without caring which
    split it originally belonged to (the CV module's loader already
    enforces split membership; duplicating that check here is
    unnecessary and would risk the two checks drifting out of sync).

    Parameters
    ----------
    model_name : str | None
        See load_text_embeddings().

    Returns
    -------
    dict
        {patient_id: embedding} for every patient across train,
        validation, and test combined. Keys are guaranteed unique
        because the three subsets are disjoint by construction
        (preprocessing.split_reports_by_patient asserts this).
    """

    lookup: Dict[str, np.ndarray] = {}

    for subset in ("train", "validation", "test"):
        lookup.update(
            load_text_embeddings(subset, model_name=model_name)
        )

    return lookup


def get_patient_embedding(
    patient_id: str,
    model_name: str | None = None,
    lookup: Dict[str, np.ndarray] | None = None,
) -> np.ndarray:
    """
    Look up a single patient's precomputed text embedding.

    Parameters
    ----------
    patient_id : str
        BraTS2020 patient folder name (e.g. "BraTS20_Training_001").
    model_name : str | None
        See load_text_embeddings(). Ignored if `lookup` is provided.
    lookup : dict | None
        Optional pre-built lookup from build_embedding_lookup(), to
        avoid reloading all three .npy files on every call when
        looking up many patients in a loop.

    Returns
    -------
    np.ndarray
        Shape (Config.EMBEDDING_DIM,).

    Raises
    ------
    KeyError
        If patient_id is not found in any subset.
    """

    if lookup is None:
        lookup = build_embedding_lookup(model_name=model_name)

    if patient_id not in lookup:
        raise KeyError(
            f"'{patient_id}' not found in any text-embedding subset "
            f"(train/validation/test). Check spelling, or use "
            f"predict.extract_text_features() if this is a new patient "
            f"not present in the TextBraTS dataset at all."
        )

    return lookup[patient_id]
